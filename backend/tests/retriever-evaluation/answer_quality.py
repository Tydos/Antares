#!/usr/bin/env python3
"""
Evaluate RAG answer quality (faithfulness and answer relevance) using Claude as judge.

Requires a gold set generated by generate_gold_set.py:
  [{"question", "ground_truth", "ground_truth_contexts", "source_pdf", "page"}, ...]

Usage (from backend/):
    python tests/retriever-evaluation/answer_quality.py --qa tests/retriever-evaluation/gold_set.json
    python tests/retriever-evaluation/answer_quality.py --qa gold_set.json --mode all --out aq_results.json
"""

from __future__ import annotations

import argparse
import json
import logging
import re
import sys
import time
from pathlib import Path

import anthropic
import requests

sys.path.insert(0, str(Path(__file__).parents[2]))

from dotenv import load_dotenv

load_dotenv(Path(__file__).parents[3] / ".env")

from src.config import settings  # noqa: E402

logging.basicConfig(level=logging.WARNING, format="%(levelname)s: %(message)s")

MODES = ["hybrid", "semantic", "keyword"]
_DEFAULT_MODEL = "claude-haiku-4-5"

_FAITHFULNESS_SYSTEM = (
    "You are an evaluation assistant for a RAG system. "
    "Rate whether a generated answer is fully supported by the provided context. "
    'Return ONLY valid JSON: {"score": <0.0-1.0>, "reasoning": "<one sentence>"}. '
    "Score 1.0 means every factual claim in the answer is directly supported by the context. "
    "Score 0.0 means the answer contains claims not found in the context (hallucinations). "
    "Use partial scores for mixed cases. Do not include any other text."
)

_RELEVANCE_SYSTEM = (
    "You are an evaluation assistant for a RAG system. "
    "Rate whether a generated answer addresses the user's question. "
    'Return ONLY valid JSON: {"score": <0.0-1.0>, "reasoning": "<one sentence>"}. '
    "Score 1.0 means the answer fully and directly addresses the question. "
    "Score 0.0 means the answer is completely off-topic or does not address the question. "
    "Use partial scores for mixed cases. Do not include any other text."
)


def parse_score(text: str) -> dict | None:
    text = text.strip()
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass
    match = re.search(r"\{.*?\}", text, re.DOTALL)
    if match:
        try:
            return json.loads(match.group())
        except json.JSONDecodeError:
            pass
    s = re.search(r'"score"\s*:\s*([0-9.]+)', text)
    r = re.search(r'"reasoning"\s*:\s*"((?:[^"\\]|\\.)+)"', text)
    if s:
        return {"score": float(s.group(1)), "reasoning": r.group(1) if r else ""}
    return None


def format_context(chunks: list[dict]) -> str:
    blocks = []
    for i, chunk in enumerate(chunks, start=1):
        page = chunk.get("page") or "?"
        header = f"[{i}] [{chunk.get('filename', 'unknown')} p.{page}]"
        blocks.append(f"{header}\n{chunk.get('content', '').strip()}")
    return "\n\n".join(blocks)


def call_api(api: str, question: str, top_k: int, mode: str) -> dict:
    resp = requests.post(
        f"{api}/query",
        json={"question": question, "top_k": top_k, "search_mode": mode},
        timeout=60,
    )
    resp.raise_for_status()
    return resp.json()


def judge_faithfulness(
    client: anthropic.Anthropic, question: str, context: str, answer: str, model: str
) -> dict:
    try:
        message = client.messages.create(
            model=model,
            max_tokens=200,
            system=_FAITHFULNESS_SYSTEM,
            messages=[
                {
                    "role": "user",
                    "content": f"Context:\n{context}\n\nQuestion: {question}\n\nAnswer: {answer}",
                }
            ],
        )
        text = message.content[0].text if message.content else ""
    except Exception as exc:
        logging.warning("Claude faithfulness call failed: %s", exc)
        return {"score": None, "reasoning": "api error"}

    parsed = parse_score(text)
    if parsed is None:
        logging.warning("Could not parse faithfulness score: %r", text[:120])
        return {"score": None, "reasoning": "parse error"}
    return parsed


def judge_answer_relevance(
    client: anthropic.Anthropic, question: str, answer: str, model: str
) -> dict:
    try:
        message = client.messages.create(
            model=model,
            max_tokens=200,
            system=_RELEVANCE_SYSTEM,
            messages=[
                {
                    "role": "user",
                    "content": f"Question: {question}\n\nAnswer: {answer}",
                }
            ],
        )
        text = message.content[0].text if message.content else ""
    except Exception as exc:
        logging.warning("Claude relevance call failed: %s", exc)
        return {"score": None, "reasoning": "api error"}

    parsed = parse_score(text)
    if parsed is None:
        logging.warning("Could not parse relevance score: %r", text[:120])
        return {"score": None, "reasoning": "parse error"}
    return parsed


def evaluate(
    api: str,
    qa: list[dict],
    mode: str,
    top_k: int,
    client: anthropic.Anthropic,
    model: str,
    delay: float,
) -> list[dict]:
    results = []
    for i, item in enumerate(qa, start=1):
        question = item["question"]
        print(f"  [{i}/{len(qa)}] {question[:80]}")

        try:
            resp = call_api(api, question, top_k, mode)
        except Exception as exc:
            logging.warning("API call failed for question %d: %s", i, exc)
            print("    -> api-error")
            results.append(
                {
                    "id": i,
                    "question": question,
                    "answer": None,
                    "chunks_returned": 0,
                    "faithfulness_score": None,
                    "faithfulness_reasoning": "api error",
                    "relevance_score": None,
                    "relevance_reasoning": "api error",
                }
            )
            continue

        answer = resp.get("answer") or ""
        chunks = resp.get("chunks") or []

        if not answer or not chunks:
            print("    -> skipped (no answer or no chunks returned)")
            results.append(
                {
                    "id": i,
                    "question": question,
                    "answer": answer,
                    "chunks_returned": len(chunks),
                    "faithfulness_score": None,
                    "faithfulness_reasoning": "no answer or no chunks",
                    "relevance_score": None,
                    "relevance_reasoning": "no answer or no chunks",
                }
            )
            if delay > 0:
                time.sleep(delay)
            continue

        context = format_context(chunks)

        faith = judge_faithfulness(client, question, context, answer, model)
        if delay > 0:
            time.sleep(delay)

        rel = judge_answer_relevance(client, question, answer, model)
        if delay > 0:
            time.sleep(delay)

        f_score = faith.get("score")
        r_score = rel.get("score")
        if f_score is not None and r_score is not None:
            print(f"    -> faithfulness={f_score:.2f}  relevance={r_score:.2f}")
        else:
            print("    -> parse error on one or more scores")

        results.append(
            {
                "id": i,
                "question": question,
                "answer": answer,
                "chunks_returned": len(chunks),
                "faithfulness_score": f_score,
                "faithfulness_reasoning": faith.get("reasoning", ""),
                "relevance_score": r_score,
                "relevance_reasoning": rel.get("reasoning", ""),
            }
        )

    return results


def print_summary(mode_results: dict[str, list[dict]]) -> None:
    print(f"\n{'MODE':<12} {'Faithfulness':>13} {'Ans.Relevance':>14} {'Scored':>7} {'Skipped':>8}")
    print("-" * 60)
    for mode, results in mode_results.items():
        scored = [
            r
            for r in results
            if r["faithfulness_score"] is not None and r["relevance_score"] is not None
        ]
        n_skipped = len(results) - len(scored)
        if not scored:
            print(f"{mode:<12} {'N/A':>13} {'N/A':>14} {'0':>7} {n_skipped:>8}")
            continue
        avg_faith = sum(r["faithfulness_score"] for r in scored) / len(scored)
        avg_rel = sum(r["relevance_score"] for r in scored) / len(scored)
        print(f"{mode:<12} {avg_faith:>13.2f} {avg_rel:>14.2f} {len(scored):>7} {n_skipped:>8}")


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Evaluate RAG answer quality using Claude as judge"
    )
    parser.add_argument("--qa", required=True, help="Path to gold set JSON")
    parser.add_argument("--api", default="http://localhost:8000", help="API base URL")
    parser.add_argument("--top-k", type=int, default=5)
    parser.add_argument(
        "--mode",
        default="hybrid",
        help="Search mode: hybrid | semantic | keyword | all",
    )
    parser.add_argument("--model", default=_DEFAULT_MODEL, help="Claude judge model")
    parser.add_argument("--delay", type=float, default=0.5, help="Seconds between Claude calls")
    parser.add_argument("--out", default=None, help="Optional JSON output path")
    args = parser.parse_args()

    if not settings.claude_token:
        print("Error: CLAUDE_TOKEN not set in .env")
        return 1

    with open(args.qa, encoding="utf-8") as f:
        data = json.load(f)

    if not isinstance(data, list) or not data or "question" not in data[0]:
        print("Error: --qa must be a gold set JSON list with 'question' fields")
        return 1

    qa = [{"question": d["question"]} for d in data]
    print(f"Loaded {len(qa)} questions from {args.qa}")
    print(f"Judge model: {args.model}  |  search mode: {args.mode}  |  top-k: {args.top_k}")

    try:
        h = requests.get(f"{args.api}/health", timeout=10)
        body = h.json()
        if body.get("services", {}).get("database") is False:
            print("Warning: database unhealthy — results may be empty")
    except requests.exceptions.ConnectionError as e:
        print(f"API unreachable: {e}")
        return 1

    client = anthropic.Anthropic(api_key=settings.claude_token)
    modes_to_run = MODES if args.mode == "all" else [args.mode]
    all_results: dict[str, list[dict]] = {}

    for mode in modes_to_run:
        print(f"\n=== Mode: {mode} ===")
        results = evaluate(args.api, qa, mode, args.top_k, client, args.model, args.delay)
        all_results[mode] = results

    print_summary(all_results)

    if args.out:
        with open(args.out, "w", encoding="utf-8") as f:
            json.dump(all_results, f, indent=2, ensure_ascii=False)
        print(f"\nSaved to {args.out}")

    return 0


if __name__ == "__main__":
    sys.exit(main() or 0)
